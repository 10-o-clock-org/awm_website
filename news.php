                <p><em>"Je kleiner der Raum, desto mehr nimmst du an Wissen mit. Danke für die tolle AI Week."</em></p>
                <p>Und die Zahl der Einreichungen überstieg mit 53 erstmals die der verfügbaren Programmslots.</p>
                <br/><br/>
 
